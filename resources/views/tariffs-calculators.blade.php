                                    For clearance of export goods, the export or his agents have to undertake the following formalities:
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (a) Registration:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">01</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The exporters have to obtain PAN based Business Identification Number(BIN) from the Directorate General of Foreign Trade prior to filing of shipping bill for clearance of export goods. Under the EDI System, PAN based BIN is received by the Customs System from the DGFT online. The exporters are also required to register authorised foreign exchange dealer code (through which export proceeds are expected to be realised) and open a current account in the designated bank for credit of any drawback incentive.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">02</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Whenever a new Airline, Shipping Line, Steamer Agent, port or airport comes into operation, they are required to be registered into the Customs System. Whenever, electronic processing of shipping bill etc. is held up on account of non-registration of these entities, the same is to be brought to the notice of Assistant/Deputy Commissioner in-charge of EDI System for registering the new entity in the system.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (b) Registration in the case of export under export promotion schemes:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">03</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            All the exporters intending to export under the export promotion scheme need to get their licences/DEEC book etc. registered at the Customs Station. For such registration, original documents are required.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (c) Processing of Shipping Bill-Non-EDI:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">04</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Under manual system, shipping bills or, as the case may be, bills of export are required to be filed in format as prescribed in the Shipping Bill and Bill of Export (Form) regulations, 1991. The bills of export are being used if clearance of export goods is taken at the Land Customs Stations. Different forms of shipping bill/bill of export have been prescribed for export of duty free goods, export of dutiable goods and export under drawback etc.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">

                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">05</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Shipping Bills are required to be filed along with all original documents such as invoice, AR-4, packing list etc. The assessing officer in the Export Department checks the value of the goods, classification under Drawback schedule in case of Drawback Shipping Bills, rate of duty/cess where applicable, exportability of goods under EXIM policy and other laws inforce. The DEEC/DEPB Shipping bills are processed in the DEEC group. In case of DEEC Shipping bills, the assessing officer verifies that the description of the goods declared in the shipping bill and invoice match with the description of the resultant product as given in the DEEC book. If the assessing officer has any doubts regarding value, description of goods, he may call for samples of the goods from the docks. He may also call for any other information required by him for processing of shipping bill. He may assess the shipping bill after visual inspection of the sample or may send it for test and pass the shipping bill provisionally.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">

                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">06</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Once, the shipping bill is passed by the Export Department, the exporter or his agent present the goods to the shed appraiser (export) in docks for examination. The shed appraiser may mark the document to a Custom officer (usually an examiner) for examining the goods. The examination is carried out under the supervision of the shed appraiser (export). If the description and other particulars of the goods are found to be as declared, the shed appraiser gives a 'let export' order, after which the exporter may contact the preventive superintendent for supervising the loading of goods on to the vessel.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">

                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">07</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Once, the shipping bill is passed by the Export Department, the exporter or his agent present the goods to the shed appraiser (export) in docks for examination. The shed appraiser may mark the document to a Custom officer (usually an examiner) for examining the goods. The examination is carried out under the supervision of the shed appraiser (export). If the description and other particulars of the goods are found to be as declared, the shed appraiser gives a 'let export' order, after which the exporter may contact the preventive superintendent for supervising the loading of goods on to the vessel.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">

                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">08</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            In case the examining staff in the docks finds some discrepancy in the goods, they may mark the shipping bill back to export department/DEEC group with their observations as well as sample of goods, if needed. The export department re-considers the case and decide whether export can be allowed, or amendment in description, value etc. is required before export and whether any other action is required to be taken under the Customs Act, 1962 for mis-declaration of description of value etc.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (d) Processing of Shipping Bill-EDI:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">09</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Under EDI System, declarations in prescribed format are to be filed through the Service Centers of Customs. A checklist is generated for verification of data by the exporter/CHA. After verification, the data is submitted to the System by the Service Center operator and the System generates a Shipping Bill Number, which is endorsed on the printed checklist and returned to the exporter/CHA. For export items which are subject to export cess, the TR-6 challans for cess is printed and given by the Service Center to the exporter/CHA immediately after submission of shipping bill. The cess can be paid on the strength of the challan at the designated bank. No copy of shipping bill is made available to exporter/CHA at this stage.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (e) Octroi procedure, Quota Allocation and Other certification for Export Goods:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">10</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The quota allocation label is required to be pasted on the export invoice. The allocation number of AEPC is to be entered in the system at the time of shipping bill entry. The quota certification of export invoice needs to be submitted to Customs along-with other original documents at the time of examination of the export cargo. For determining the validity date of the quota, the relevant date needs to be the date on which the full consignment is presented to the Customs for examination and duly recorded in the Computer System. In EDI System at Delhi Air cargo, the quota information is automatically verified from the AEPC/TEXPROCIL system.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">

                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">11</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Since the shipping bill is generated only after the 'let export order' is given by Customs, the exporter may make use of export invoice or such other document as required by the Octroi authorities for the purpose of Octroi exemption.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (f) Arrival of Goods at Docks:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">12</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The goods brought for the purpose of examination and subsequent 'let export' is allowed entry to the Dock on the strength of the checklist and other declarations filed by the exporter in the Service Center. The Port authorities have to endorse the quantity of goods actually received on the reverse of the Check List.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (g) System Appraisal of Shipping Bills:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">13</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            In many cases the Shipping Bill is processed by the system on the basis of declarations made by the exporters without any human intervention. In other cases where the Shipping Bill is processed on screen by the Customs Officer, he may call for the samples, if required for confirming the declared value or for checking classification under the Drawback Schedule. He may also give any special instructions for examination of goods, if felt necessary.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (h) Status of Shipping Bill:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">14</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The exporter/CHA can check up with the query counter at the Service Center whether the Shipping Bill submitted by them in the system has been cleared or not, before the goods are brought into the Docks for examination and export. In case any query is raised, the same is required to be replied through the service center or in case of CHAs having EDI connectivity through their respective terminals. The Customs officer may pass the Shipping Bill after all the queries have been satisfactorily replied to.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (i) Customs Examination of Export Cargo:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">15</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            After the receipt of the goods in the dock, the exporter/CHA may contact the Customs Officer designated for the purpose present the check list with the endorsement of Port Authority and other declarations as aforesaid along with all original documents such as, Invoice and Packing list, AR-4, etc. Customs Officer may verify the quantity of the goods actually received and enter into the system and thereafter mark the Electronic Shipping Bill and also hand over all original documents to the Dock Appraiser of the Dock who many assign a Customs Officer for the examination and intimate the officers' name and the packages to be examined, if any, on the check list and return it to the exporter or his agent.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">16</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The Customs Officer may inspect/examine the shipment along with the Dock Appraiser. The Customs Officer enters the examination report in the system. He then marks the Electronic Bill along with all original documents and checklist to the Dock Appraiser. If the Dock Appraiser is satisfied that the particulars entered in the system conform to the description given in the original documents and as seen in the physical examination, he may proceed to allow "let export" for the shipment and inform the exporter or his agent.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (j) Variation Between the Declaration & Physical Examination:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">17</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The check list and the declaration along with all original documents is retained by the Appraiser concerned. In case of any variation between the declaration in the Shipping Bill and physical documents/examination report, the Appraiser may mark the Electronic Shipping Bill to the Assistant Commissioner/Deputy Commissioner of Customs (Exports). He may also forward the physical documents to Assistant Commissioner/Deputy Commissioner of Customs (Exports) and instruct the exporter or his agent to meet the Assistant Commissioner/Deputy Commissioner of Customs (Exports) for settlement of dispute. In case the exporter agrees with the views of the Department, the Shipping Bill needs to be processed accordingly. Where, however, the exporter disputes the view of the Department principles of natural justice is required to be followed before finalisation of the issue.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (k) Stuffing / Loading of Goods in Containers
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">18</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The exporter or his agent should hand over the exporter copy of the shipping bill duly signed by the Appraiser permitting "Let Export" to the steamer agent who may then approach the proper officer (Preventive Officer) for allowing the shipment. In case of container cargo the stuffing of container at Dock is dome under Preventive Supervision. Loading of both containerized and bulk cargo is done under Preventive Supervision. The Customs Preventive Superintendent (Docks) may enter the particulars of packages actually stuffed in to the container, the bottle seal number particulars of loading of cargo container on board into the system and endorse these details on the exporter copy of the shipping bill presented to him by the steamer agent. If there is a difference in the quantity/number of packages stuffed in the containers/goods loaded on vessel the Superintendent (Docks) may put a remark on the shipping bill in the system and that shipping bill requires amendment or changed quantity. Such shipping bill also may not be taken up for the purpose of sanction of Drawback/DEEC logging, till the shipping bill is suitably amended for the changed quantity. The Customs Preventive Officer supervising the loading of container and general cargo in to the vessel may give "Shipped on Board" endorsement on the exporters copy of the shipping bill.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (l) Drawal of Samples:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">19</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Where the Appraiser Dock (export) orders for samples to be drawn and tested, the Customs Officer may proceed to draw two samples from the consignment and enter the particulars thereof along with details of the testing agency in the ICES/E system. There is no separate register for recording dates of samples drawn. Three copies of the test memo are prepared by the Customs Officer and are signed by the Customs Officer and Appraising Officer on behalf of Customs and the exporter or his agent. The disposals of the three copies of the test memo are as follows: -
                                        </p>
                                        <ul>
                                            <li>Original – to be sent along with the sample to the test agency.</li>
                                            <li>Duplicate – Customs copy to be retained with the 2nd sample.</li>
                                            <li>Triplicate – Exporter’s copy.</li>
                                        </ul>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">20</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The Assistant Commissioner/Deputy Commissioner if he considers necessary, may also order for sample to be drawn for purpose other than testing such as visual inspection and verification of description, market value inquiry, etc.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (m) Amendments:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">21</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Any correction/amendments in the checklist generated after filing of declaration can be made at the service center, provided, the documents have not yet been submitted in the system and the shipping bill number has not been generated. Where corrections are required to be made after the generation of the shipping bill No. or after the goods have been brought into the Export Dock, amendments is carried out in the following manners.
                                        </p>
                                        <ul>
                                            <li>If the goods have not yet been allowed “let export” amendments may be permitted by the Assistant Commissioner (Exports).</li>
                                            <li>Where the “Let Export” order has already been given, amendments may be permitted only by the Additional/Joint Commissioner, Custom House, in charge of export section.</li>

                                        </ul>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">22</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            In both the cases, after the permission for amendments has been granted, the Assistant Commissioner/Deputy Commissioner (Export) may approve the amendments on the system on behalf of the Additional /Joint Commissioner. Where the print out of the Shipping Bill has already been generated, the exporter may first surrender all copies of the shipping bill to the Dock Appraiser for cancellation before amendment is approved on the system.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (n) Export of Goods Under Claim for Drawback:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">23</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            After actual export of the goods, the Drawback claim is processed through EDI system by the officers of Drawback Branch on first come first served basis. There is no need for filing separate drawback claims. The status of the shipping bills and sanction of DBK claim can be ascertained from the query counter set up at the service center. If any query has been raised or deficiency noticed, the same is shown on the terminal. A print out of the query/deficiency may be obtained by the authorized person of the exporter from the service center. The exporters are required to reply to such queries through the service center. The claim will come in queue of the EDI system only after reply to queries/deficiencies are entered by the Service Center.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">24</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            All the claims sanctioned on a particular day are enumerated in a scroll and transferred to the Bank through the system. The bank credits the drawback amount in the respective accounts of the exporters. Bank may send a fortnightly statement to the exporters of such credits made in their accounts.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">25</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The Steamer Agent/Shipping Line may transfer electronically the EGM to the Customs EDI system so that the physical export of the goods is confirmed, to enable the Customs to sanction the drawback claims.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (o) Generation of Shipping Bills:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">26</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            After the “let export” order is given on the system by the Appraiser, the Shipping Bill is generated by the system in two copies i.e., one Customs copy, one exporter’s copy (E.P. copy is generated after submission of EGM). After obtaining the print out the appraiser obtains the signatures of the Customs Officer on the examination report and the representative of the CHA on both copies of the shipping bill and examination report. The Appraiser thereafter signs & stamps both the copies of the shipping bill at the specified place.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">27</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The Appraiser also signs and stamps the original & duplicate copy of SDF. Customs copy of shipping bill and original copy of the SDF is retained along with the original declarations by the Appraiser and forwarded to Export Department of the Custom House. He may return the exporter copy and the second copy of the SDF to the exporter or his agent.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">28</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            As regards the AEPC quota and other certifications, these are retained along with the shipping bill in the dock after the shipping bill is generated by the system. At the time of examination, apart from checking that the goods are covered by the quota certifications, the details of the quota entered into the system needs to be checked.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="exportsubtxt">
                                        (p) Export General Manifest:
                                    </div>
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">29</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            All the shipping lines/agents need to furnish the Export General Manifests, Shipping Bill wise, to the Customs electronically within 7 days from the date of sailing of the vessel.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">30</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            Apart from lodging the EGM electronically the shipping lines need to continue to file manual EGMs along with the exporter copy of the shipping bills as per the present practice in the export department. The manual EGMs need to be entered in the register at the Export Department and the Shipping lines may obtain acknowledgements indicating the date and time at which the EGMs were received by the Export Department.
                                        </p>
                                    </div>
                                </div>
                                <div class="importcontentblk">
                                    <div class="calnumblk ibvt">
                                        <div class="calnumblue">31</div>
                                    </div>
                                    <div class="calcontentblk ibvt">
                                        <p class="pagetxt">
                                            The above is the general procedure for export under EDI Systems. However special procedures exist for specified schemes, details of which may be obtained from the Public Notice/Standing Orders issued by the respective Commissionerates.
                                        </p>
                                    </div>
                                </div>
                            </div>
